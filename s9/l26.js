// operatori matematici unari ++ e --

var y = 0;
var num = 5;

// operatore ++
y = num++;
console.log(y);
console.log(num);

y = ++num;
console.log(y);
console.log(num);

// operatore --
y = num--;
console.log(y);
console.log(num);

y = --num;
console.log(y);
console.log(num);
